package com.sorting.demo;

import java.util.ArrayList;
import java.util.Collections;
import java.util.LinkedList;
import java.util.List;

public class SortExample {

	public static void main(String[] args) 
	{
		List<String> names = new ArrayList<String>();
		names.add("EEE");
		names.add("ZZZ");
		names.add("AAA");
		names.add("WWW");
		
		// Printing the unsorted list  
        System.out.println("The elements of the list before performing sorting:\n" + names);
        
        /* Sorting the names array list in ascending order */  
        Collections.sort(names);
        
     // Printing the sorted list  
        System.out.println("\nThe elements of the list after performing sorting using Collections.sort() method :\n" + names);  
          
        // Creating a linked list of integer values  
        LinkedList<Integer> age = new LinkedList<Integer>();   
        age.add(23);   
        age.add(25);   
        age.add(22);   
        age.add(24);   
        age.add(27);   
    
        // Printing the unsorted linked list  
        System.out.println("\nThe elements of the linked list before performing sorting:\n" + age);  
          
        /* Sorting the age linked-list in ascending order */  
        Collections.sort(age);   
    
        // Printing the sorted linked list  
        System.out.println("\nThe elements of the linked list after performing sorting using Collections.sort() method :\n" + age);       
    }   
		

	
}
